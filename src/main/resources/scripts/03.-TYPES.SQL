-- PACKAGE HEADER
CREATE OR REPLACE PACKAGE PKG_TYPES
  AS
  TYPE C_CURSOR IS REF CURSOR;

  PROCEDURE SP_FIND_PET_TYPES(P_CURSOR OUT C_CURSOR);

END PKG_TYPES;

-- PACKAGE BODY
CREATE OR REPLACE PACKAGE BODY PKG_TYPES
  AS

  PROCEDURE SP_FIND_PET_TYPES(P_CURSOR OUT C_CURSOR)
    AS
    BEGIN
      OPEN P_CURSOR
      FOR
      SELECT ID,
             NAME
        FROM TYPES
        ORDER BY NAME ASC;
    END SP_FIND_PET_TYPES;

END PKG_TYPES;
